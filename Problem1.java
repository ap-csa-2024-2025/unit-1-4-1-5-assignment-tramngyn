public class Problem1
{
  public static void main(String[] args)
  {
    int value = 24;
    System.out.println("Initial value: " + value);

    value++;
    System.out.println("number is now " + value); // 25 

    value++;
    System.out.println("number is now " + value); // 26

    value++;
    System.out.println("number is now " + value); // 27

    value++;
    System.out.println("number is now " + value); // 28

    value--;
    System.out.println("number is now " + value); // 27

    value--;
    System.out.println("number is now " + value); // 26

    value--;
    System.out.println("number is now " + value); // 25

    value--;
    System.out.println("number is now " + value); // 24
  }
}
